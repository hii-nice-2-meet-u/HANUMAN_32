void SPI_Device_Class::begin(void)
{
	if (!SPI_Device_Class::is_SPI_Begin)
	{
		privSPI_Shared->begin();
		privSPI_Shared->setHwCs(false);
		privSPI_Shared->setBitOrder(MSBFIRST);
		privSPI_Shared->setDataMode(SPI_MODE0);
		SPI_Device_Class::is_SPI_Begin = true;
	}
}

